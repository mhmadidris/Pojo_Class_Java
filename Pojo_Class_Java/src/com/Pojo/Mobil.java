package com.Pojo;

class Mobil {
    private String mobil_pertama;
    private String mobil_kedua;
    private String mobil_ketiga;
    private String mobil_keempat;



    public void setMobil_pertama(String mobil_pertama) {
        this.mobil_pertama = mobil_pertama;
    }

    public String getMobil_pertama() {
        return mobil_pertama;
    }

    public void setMobil_kedua (String mobil_kedua) {
        this.mobil_kedua = mobil_kedua;
    }

    public String getMobil_kedua() {
        return mobil_kedua;
    }

    public void setMobil_ketiga(String mobil_ketiga) {
        this.mobil_ketiga = mobil_ketiga;
    }

    public String getMobil_ketiga() {
        return mobil_ketiga;
    }

    public void setMobil_keempat(String mobil_keempat) {
        this.mobil_keempat = mobil_keempat;
    }


    public String getMobil_keempat() {
        return mobil_keempat;
    }
}